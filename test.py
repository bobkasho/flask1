#   должен ответить на пять вопросов. Каждый вопрос строится
#   из двух случайно сгенерированных целых чисел (например, [num1] + [num2]). !!!ПО ХОДУ ТУТ РЕЧЬ ПРО СУММУ РАНДОМНЫХ ЧИСЕЛ!!!
#   Предложите пользователю ввести ответ. Если пользователь ввел правильный ответ, добавьте
#   одно очко в его пользу. В конце игры сообщите пользователю количество правильных ответов

import random
xxx = 1
victory_count = 0
while xxx < 10:
    print("Угадай сумму случайных чисел (до 10). У тебя 10 попыток. Попытка номер", xxx)
    num1 = random.randint(1, 5)
    num2 = random.randint(1, 5)
    num = num1 + num2
    answer = int(input("Введите число от 1 до 10: "))
    xxx = xxx + 1
    if answer == num:
        print("Отлично, угадал, сохраняю результат!")
        victory_count = victory_count + 1
    else:
        print("Не угадал! Было число", num)
print("Удачных попыток", victory_count)


